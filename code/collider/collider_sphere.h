#pragma once
#include "collider.h"

class GameObject;
//--------------------------------------------------------------------------------
// クラス
//--------------------------------------------------------------------------------
class ColliderSphere :
    public Collider
{
public:
    ColliderSphere(GameObject* game_object):Collider(game_object, "ColliderSphere",COLLIDER_SPHERE),m_radius(1.0f){}
    ~ColliderSphere();
    //--------------------------------------------------------------------------------
    // 半径関数
    //--------------------------------------------------------------------------------
    void SetRadius(const float& radius){ m_radius = radius; }
    auto& GetRadius()const { return m_radius; }
private:
    //--------------------------------------------------------------------------------
    // メンバー変数
    //--------------------------------------------------------------------------------
    float m_radius;
};

