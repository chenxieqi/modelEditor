#pragma once

#include <list>

class Light;

using namespace std;
//--------------------------------------------------------------------------------
// クラス
//--------------------------------------------------------------------------------
class LightManager
{
public:
    LightManager();
    ~LightManager();
    //--------------------------------------------------------------------------------
    // 基本関数
    //--------------------------------------------------------------------------------
    void Init();
    void Uninit();
    //--------------------------------------------------------------------------------
    // ゲットメインライト
    //--------------------------------------------------------------------------------
    Light* GetMainLight() { return m_lights.front(); }
    //--------------------------------------------------------------------------------
    // 登録
    //--------------------------------------------------------------------------------
    void Regiester(Light* light) { m_lights.push_back(light); }
    //--------------------------------------------------------------------------------
    // 生成
    //--------------------------------------------------------------------------------
    static LightManager* Create();
private:
    //--------------------------------------------------------------------------------
    // メンバー変数
    //--------------------------------------------------------------------------------
    list<Light*> m_lights;
};

