#pragma once
#include <string>

class GameObject;
//--------------------------------------------------------------------------------
// クラス
//--------------------------------------------------------------------------------
class Component
{
public:
    Component(GameObject* game_object,std::string name);
    ~Component();
    //--------------------------------------------------------------------------------
    // 基本関数
    //--------------------------------------------------------------------------------
    virtual void Init() {}
    virtual void Uninit() {}
    virtual void Update() {}
    virtual void LateUpdate(){}
    //--------------------------------------------------------------------------------
    // ゲッター
    //--------------------------------------------------------------------------------
    auto& GetName() const{ return m_name; }
    auto& GetGameObject()const { return m_game_object; }
protected:
    //--------------------------------------------------------------------------------
    // メンバー変数
    //--------------------------------------------------------------------------------
    std::string m_name;
    GameObject* m_game_object;
};

